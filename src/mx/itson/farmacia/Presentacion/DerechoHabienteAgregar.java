    private void btnRegistrarDhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarDhActionPerformed
        // TODO add your handling code here:
        
        String nombre = txtNombreDh.getText().trim();
        String apellido = txtApellidoDh.getText().trim();
        String domicilio = txtDomicilioDh.getText().trim();
        String ciudad = txtCiudadDh.getText().trim();
        String telefono = txtTelefonoDh.getText().trim();
        Doctor doctor = (Doctor) cbxAsignarDoctorDh.getSelectedItem();
        int edad = Integer.parseInt(txtEdadDh.getText().trim());
        int afiliacion = Integer.parseInt(txtAfiliacionDh.getText().trim());
        
        String dependencia = txtDependenciaDh.getText().trim();
        String fechaIngreso = txtFechaIngresoDh.getText().trim();
        
        DerechoHabiente dh = new DerechoHabiente( nombre, apellido, domicilio, ciudad, telefono, edad, afiliacion,doctor, dependencia, fechaIngreso);
        if(nombre.isEmpty() || apellido.isEmpty() || domicilio.isEmpty() || ciudad.isEmpty() || telefono.isEmpty() || dependencia.isEmpty() || fechaIngreso.isEmpty()){
           
            JOptionPane.showMessageDialog(null, "No se pueden dejar cajas de texto vacias.");
               
        }else {
            dhi.agregarDerechoHabiente(dh);
            dr.agregarDerechoHabienteLista(dh, doctor);
            
            //Limpiar cajas de texto
            txtNombreDh.setText(null);
            txtApellidoDh.setText(null);
            txtDomicilioDh.setText(null);
            txtCiudadDh.setText(null);
            txtEdadDh.setText(null);
            txtTelefonoDh.setText(null);
            txtAfiliacionDh.setText(null);
        }
       
        
    }//GEN-LAST:event_btnRegistrarDhActionPerformed

